std::string Elm327CommandRepository::getCommandByPid(ObdCommandPid pid)
{
    auto commandMap = getCommandMap();
    auto iter = commandMap.find(pid);
    if (iter != commandMap.end()) {
        return iter->second;
    }
    return {};
}
